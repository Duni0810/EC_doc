//----------------------------------------------------------------------------
// Set&Get Status for Next Port60 Data Handle
//----------------------------------------------------------------------------
bit SetGetPortPM2Data(void)
{
    if ( PM2Step == 0x00 )
    {
        PM2Step=0x01;   // Set Next Port60 Data Handle again
        return(0);
    }
    else
    {
        PM2Step=0x00;   // Set Command Finished
        return(1);
    }

}

//----------------------------------------------------------------------------
// Special Handle
// Get 1 Bytes PM Controller Data, PM_DATA (R/W)
//----------------------------------------------------------------------------
BYTE OEM_GetPortPM2Data(void)
{

    WORD  iLOOP;
    iLOOP = WaitPM2DataDelay;

    do
    {
        if ( IS_BIT_SET(PM2STS,1) )
        {
            if ( PM2STS & C_D )
            {
                return 0x00;
            }
            else
            {
                PM2Data = PM2DI;            // Load data
                #if  SUPPORTED_RECORDER
                if( En_Record68 )   {   RamDebug(0x68); RamDebug(PM2Data);  }
                #endif

                return 0x01;
            }
        }

        iLOOP--;
    }
    while (iLOOP != 0 );

    return 0x00;

}
//----------------------------------------------------------------------------
// Service Power Management Controller Interface
//----------------------------------------------------------------------------
void Service_PCI3(void)
{
    if ( PM2STS & C_D2 )
    {
        PM2Cmd  = PM2DI;
        PM2Step = 0;
        #if  SUPPORTED_RECORDER
        if( En_Record6C )   {   RamDebug(0x6C); RamDebug(PM2Cmd);   }
        #endif
        (Port6C_Table[PM2Cmd>>4])();
    }
    else
    {
        PM2Data = PM2DI;
        if ( PM2Step != 0x00 )
        {
            #if  SUPPORTED_RECORDER
            if( En_Record68 )   {   RamDebug(0x68); RamDebug(PM2Data);  }
            #endif
             // Pls use PM2Step for Port 6C Routine Command
            (Port6C_Table[PM2Cmd>>4]());

        }
        else
        {
            #if  SUPPORTED_RECORDER
            if( En_Record68 )   {   RamDebug(0x68); RamDebug(PM2Data);  }
            #endif
            while( PM2UnProcessCnt < 0xFF )
            {
                PM2UnProcessCnt++;
            }
        }

    }
}

