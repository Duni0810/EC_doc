//----------------------------------------------------------------------------
// Special Handle
// Get 1 Bytes Embedded Controller Data, EC_DATA (R/W)
//----------------------------------------------------------------------------
BYTE OEM_GetPort62Data(void)
{

    WORD  iLOOP;
    iLOOP = WaitECDataDelay;

    do
    {
        if ( IS_BIT_SET(PM1STS,1) )
        {
            if ( PM1STS & C_D )
            {
                return 0x00;
            }
            else
            {
                PM1Data = PM1DI;            // Load data
                return 0x01;
            }
        }

        iLOOP--;
    }
    while (iLOOP != 0 );

    return 0x00;

}
